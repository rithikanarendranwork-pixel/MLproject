class SelfCorrectionEnv:
    """
    State  : sentence embedding of (question + current answer) + normalised
             revision count
    Actions: 0 = STOP  |  1 = REVISE
"""

    def __init__(self, dataset, max_revisions: int = 3):
        self.dataset       = dataset
        self.max_revisions = max_revisions

    def reset(self):
        self.sample    = random.choice(self.dataset)
        self.question  = self.sample["question"]
        self.solution  = self.sample["answer"]
        self.true_ans  = extract_gsm8k_answer(self.solution)
        self.revisions = 0
        self.answer    = call_llm(self.question)
        return self._get_state()

    def _get_state(self) -> torch.Tensor:
        text      = self.question + " " + (self.answer or "")
        embedding = embed_model.encode(text)
        state     = torch.tensor(embedding, dtype=torch.float32)
        rev_feat  = torch.tensor(
            [self.revisions / self.max_revisions], dtype=torch.float32
        )
        return torch.cat([state, rev_feat])

    def step(self, action: int):
        done = False

        if action == 0 or self.revisions >= self.max_revisions:
            done    = True
            correct = answers_match(extract_number(self.answer), self.true_ans)
            reward  = 1.0 if correct else -0.30      # ← changed from +0.05
            return self._get_state(), reward, done

        prev_correct   = answers_match(extract_number(self.answer), self.true_ans)
        self.answer    = call_llm(self.question, self.answer)
        self.revisions += 1
        new_correct    = answers_match(extract_number(self.answer), self.true_ans)

        if   new_correct and not prev_correct:  reward = +0.80
        elif prev_correct and not new_correct:  reward = -0.50  # ← was -0.10
        elif prev_correct and new_correct:      reward = +0.10
        else:                                   reward = -0.05  # ← was +0.00

        return self._get_state(), reward, done

class PolicyNet(nn.Module):
    def __init__(self, input_dim: int):
        super().__init__()
        self.net = nn.Sequential(
            nn.Linear(input_dim, 256),
            nn.ReLU(),
            nn.Dropout(0.1),
            nn.Linear(256, 128),
            nn.ReLU(),
            nn.Linear(128, 2),
            nn.Softmax(dim=-1),
        )

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        probs = self.net(x)
        probs = torch.clamp(probs, min=1e-8)
        return probs / probs.sum()


def train(
    env: SelfCorrectionEnv,
    policy: PolicyNet,
    episodes: int = 600,
    entropy_coef: float = 0.01,
    baseline_decay: float = 0.95,
) -> list[float]:
    """
    Improvements over vanilla REINFORCE
    ------------------------------------
    baseline     : exponential moving average of episode returns, subtracted
                   from each return before computing the loss.  Reduces variance
                   without introducing bias (unlike a learned value function).
    entropy_coef : adds β·H(π) to the objective, encouraging exploration early
                   in training and preventing premature policy collapse.
    """
    optimizer  = optim.Adam(policy.parameters(), lr=1e-3)
    gamma      = 0.99
    reward_log = []
    baseline   = 0.0   # running EMA of episode return

    for ep in range(episodes):
        state     = env.reset()
        log_probs = []
        entropies = []
        rewards   = []
        done      = False

        while not done:
            state = state.to(device)
            probs = policy(state)
            dist  = torch.distributions.Categorical(probs)
            action = dist.sample()

            log_probs.append(dist.log_prob(action))
            entropies.append(dist.entropy())

            next_state, reward, done = env.step(action.item())
            rewards.append(reward)
            state = next_state

        # Discounted returns
        G       = 0.0
        returns = []
        for r in reversed(rewards):
            G = r + gamma * G
            returns.insert(0, G)

        ep_return = returns[0]
        baseline  = baseline_decay * baseline + (1 - baseline_decay) * ep_return

        returns_t = torch.tensor(returns, dtype=torch.float32).to(device)
        # Subtract running baseline
        returns_t = returns_t - baseline
        if len(returns_t) > 1:
            returns_t = returns_t / (returns_t.std() + 1e-8)

        policy_loss  = torch.stack(
            [-lp * R for lp, R in zip(log_probs, returns_t)]
        ).sum()
        entropy_loss = -entropy_coef * torch.stack(entropies).sum()
        loss         = policy_loss + entropy_loss

        optimizer.zero_grad()
        loss.backward()
        nn.utils.clip_grad_norm_(policy.parameters(), max_norm=2.0)
        optimizer.step()

        ep_reward = sum(rewards)
        reward_log.append(ep_reward)

        if ep % 20 == 0:
            window = reward_log[-20:]
            avg    = sum(window) / len(window)
            print(
                f"Episode {ep:4d} | Reward: {ep_reward:+.3f} | "
                f"20-ep avg: {avg:+.3f} | Baseline: {baseline:+.3f} | "
                f"Revisions: {env.revisions}"
            )

    return reward_log

@torch.no_grad()
def evaluate(dataset, policy: PolicyNet, n: int = 50) -> dict:
    policy.eval()
    env = SelfCorrectionEnv(dataset, max_revisions=3)

    results      = {"zero_shot": 0, "always_revise": 0, "rl_policy": 0}
    total_reward = {"rl_policy": 0.0}
    samples      = random.sample(list(dataset), n)

    for sample in samples:
        question = sample["question"]
        solution = sample["answer"]
        true_ans = extract_gsm8k_answer(solution)
        #zero shot
        ans0 = call_llm(question)
        if answers_match(extract_number(ans0), true_ans):
            results["zero_shot"] += 1

        # always revise
        ans = ans0
        for _ in range(3):
            ans = call_llm(question, ans)
        if answers_match(extract_number(ans), true_ans):
            results["always_revise"] += 1

        # RL policy
        env.question  = question
        env.solution  = solution
        env.true_ans  = true_ans
        env.answer    = ans0
        env.revisions = 0
        state         = env._get_state()
        done          = False
        ep_reward     = 0.0

        while not done:
            probs  = policy(state.to(device))
            action = probs.argmax().item()
            state, reward, done = env.step(action)
            ep_reward += reward

        if answers_match(extract_number(env.answer), true_ans):
            results["rl_policy"] += 1
        total_reward["rl_policy"] += ep_reward

    pct = {k: v / n * 100 for k, v in results.items()}
    avg_rl_reward = total_reward["rl_policy"] / n

    for k, v in pct.items():
        print(f"  {k:<20s}: {v:.1f}%  ({results[k]}/{n})")
    print(f"\n  RL Policy avg episode reward : {avg_rl_reward:+.4f}")
    return pct

