def generate_synthetic_dataset(n: int = 300) -> list[dict]:
    """Single-digit arithmetic; answer in GSM8K '#### N' format."""
    ops = {
        "+": lambda a, b: a + b,
        "-": lambda a, b: a - b,
        "*": lambda a, b: a * b,
    }
    samples = []
    for _ in range(n):
        op_sym = random.choice(list(ops.keys()))
        a = random.randint(1, 9)
        b = random.randint(1, 9)
        if op_sym == "-" and b > a:
            a, b = b, a
        result   = ops[op_sym](a, b)
        question = f"What is {a} {op_sym} {b}?"
        answer   = f"The answer is {result}. #### {result}"
        samples.append({"question": question, "answer": answer})
    return samples


