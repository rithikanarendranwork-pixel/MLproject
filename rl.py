def extract_number(text: str) -> str | None:
    if text is None:
        return None
    if "####" in text:
        return text.split("####")[-1].strip().replace(",", "")
    nums = re.findall(r"-?\d[\d,]*\.?\d*", text)
    return nums[-1].replace(",", "") if nums else None


def answers_match(pred: str | None, true: str | None) -> bool:
    if pred is None or true is None:
        return False
    try:
        return abs(float(pred) - float(true)) < 1e-3
    except ValueError:
        return pred.strip() == true.strip()

class SimulatedSolver:
    """
    Mimics an imperfect LLM with realistic and learnable correction behaviour.

    Parameters
    ----------
    initial_accuracy : float  – P(correct) on first call
    correction_rate  : float  – P(wrong -> correct) on revision
    degradation_rate : float  – P(correct -> wrong) on revision
    """

    def __init__(
        self,
        initial_accuracy: float = 0.70,
        correction_rate:  float = 0.75,
        degradation_rate: float = 0.04,
    ):
        self.initial_accuracy = initial_accuracy
        self.correction_rate  = correction_rate
        self.degradation_rate = degradation_rate
        self._true_cache: dict[str, float] = {}

    def _true_value(self, question: str) -> float:
        if question in self._true_cache:
            return self._true_cache[question]
        m = re.match(r"What is (\d+) ([+\-*]) (\d+)\?", question)
        result = eval(f"{m.group(1)}{m.group(2)}{m.group(3)}") if m else 0
        self._true_cache[question] = float(result)
        return float(result)

    def _wrong_answer(self, true_val: float) -> float:
        delta = random.choice([-3, -2, -1, 1, 2, 3])
        wrong = true_val + delta
        if abs(wrong - true_val) < 1e-3:
            wrong += 1
        return wrong

    def __call__(self, question: str, prev_answer: str | None = None) -> str:
        true_val = self._true_value(question)

        if prev_answer is None:
            value = (true_val if random.random() < self.initial_accuracy
                     else self._wrong_answer(true_val))
        else:
            prev_num     = extract_number(prev_answer)
            prev_correct = answers_match(prev_num, str(true_val))
            if prev_correct:
                value = (self._wrong_answer(true_val)
                         if random.random() < self.degradation_rate
                         else true_val)
            else:
                value = (true_val if random.random() < self.correction_rate
                         else self._wrong_answer(true_val))

        int_val = int(round(value))
        return f"The answer is {int_val}. #### {int_val}"


solver = SimulatedSolver(
    initial_accuracy=0.70,
    correction_rate=0.75,
    degradation_rate=0.04,
)

def call_llm(question: str, prev_answer: str | None = None) -> str:
    return solver(question, prev_answer)

